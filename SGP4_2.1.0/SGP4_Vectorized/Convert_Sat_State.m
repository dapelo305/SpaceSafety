%-------------------------------------------------------------------
%------------------------- Convert_Sat_State -----------------------
%-------------------------------------------------------------------
function [p, v] = Convert_Sat_State(pos, vel)

xkmper = 6378.135;

p = zeros(3,1);
v = zeros(3,1);

for i=1:3
    p(i) = pos(i) * xkmper;
    v(i) = vel(i) * xkmper / 60;
end

